    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um curso!");
        } else{
            categoria = new Categoria();
            categoria.setCodigoCategoria(Integer.parseInt(txtId.getText()));
            int confirma = JOptionPane.showConfirmDialog(null, "Deseja excluir: " +txtCategoria.getText());
            
            if(confirma ==0){
                
                
                
                try {
                    categoriaDAO.excluir(categoria);
                } catch (SQLException ex) {
                    Logger.getLogger(CategoriaView.class.getName()).log(Level.SEVERE, null, ex);
                }
                
               
                JOptionPane.showMessageDialog(null, "Excluido com sucesso!");
                
                atualizarTabelaCategoria();
                limpaCamposCategoria();
                preparaExcluir();
            }
        }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

