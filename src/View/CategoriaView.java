     public void atualizarTabelaCategoria(){
        categoria = new Categoria();
        

       try {
           listaCategoria = categoriaDAO.ListaCategoria();
       } catch (SQLException ex) {
           Logger.getLogger(CategoriaView.class.getName()).log(Level.SEVERE, null, ex);
       }

 
        String dados[][] = new String[listaCategoria.size()][2];
        int i = 0;
        for(Categoria categoria : listaCategoria){
            dados[i][0] = String.valueOf(categoria.getCodigoCategoria());
            dados[i][1] = categoria.getNomeCategoria();
            i++;
        }
        
        String tituloColuna[] = {"ID", "CATEGORIA"};
        DefaultTableModel tabelaCategoria = new DefaultTableModel();
        tabelaCategoria.setDataVector(dados, tituloColuna);
        tbCategoria.setModel(new DefaultTableModel(dados, tituloColuna){
            boolean[] canEdit = new boolean[]{
                false, false
            };
            
            public boolean isCellEditable(int rowIndex, int columnIndex){
                return canEdit[columnIndex];
            }
        });
        
        tbCategoria.getColumnModel().getColumn(0).setPreferredWidth(70);
        tbCategoria.getColumnModel().getColumn(1).setPreferredWidth(500);
        
        
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        tbCategoria.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tbCategoria.setRowHeight(25);
        tbCategoria.updateUI();
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtCategoria.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos!");
            txtCategoria.requestFocusInWindow();
        } else if(txtId.getText().isEmpty()){
            categoria = new Categoria();
            categoria.setNomeCategoria(txtCategoria.getText());
            
            
            try {
                categoriaDAO.salvar(categoria);
            } catch (SQLException ex) {
                Logger.getLogger(CategoriaView.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
           JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
           atualizarTabelaCategoria();
           preparaSalvareCancelar();
           desativaCampos();
           limpaCamposCategoria();
        } 
        else{
            categoria = new Categoria();
            categoria.setCodigoCategoria(Integer.parseInt(txtId.getText()));
            categoria.setNomeCategoria(txtCategoria.getText());

            
            
            try {
                categoriaDAO.alterar(categoria);
            } catch (SQLException ex) {
                Logger.getLogger(CategoriaView.class.getName()).log(Level.SEVERE, null, ex);
            }
            
           
           
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            atualizarTabelaCategoria();
            preparaSalvareCancelar();
            desativaCampos();
            limpaCamposCategoria();
            
        } 
        
    }//GEN-LAST:event_btnSalvarActionPerformed

