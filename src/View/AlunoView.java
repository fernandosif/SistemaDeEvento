    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        aluno = new Aluno();
        String strRa = "__-_____";
        
        try {
                aluno = alunoDAO.recuperar(txtRa.getText());
            } catch (SQLException ex) {
                Logger.getLogger(AlunoView.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        if(aluno!=null){
            JOptionPane.showMessageDialog(null, "RA j√° Cadastrado!");
            txtRa.setText("");
            txtRa.requestFocusInWindow();
        } else if(txtRa.getText().isEmpty() || strRa.equals(txtRa.getText())){
            JOptionPane.showMessageDialog(null, "Preencha o RA!");
            txtRa.requestFocusInWindow();
            
        } else if(txtNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome!");
            txtNome.requestFocusInWindow();
        } else if(txtCurso.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Curso!");
            txtCurso.requestFocusInWindow();
        } else if(txtTurma.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Turma!");
            txtTurma.requestFocusInWindow();
        } else if(txtEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o E-Mail!");
            txtEmail.requestFocusInWindow();
        } else if(txtCodigo.getText().isEmpty()){
            aluno = new Aluno();
            aluno.setNomeAluno(txtNome.getText());
            aluno.setEmailAluno(txtEmail.getText());
            aluno.setCategoriaAluno("Aluno");
            aluno.setRaAluno(txtRa.getText());
            aluno.setCursoAluno(txtCurso.getText());
            aluno.setTurmaAluno(txtTurma.getText());

            try {
                alunoDAO.salvar(aluno);
            } catch (SQLException ex) {
                Logger.getLogger(AlunoView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            atualizarTabelaAluno();
            preparaSalvareCancelar();
            desativaCampos();
            limpaCamposAluno();
            
            
        } else{
            aluno = new Aluno();
            aluno.setCodigoAluno(Integer.parseInt(txtCodigo.getText()));
            aluno.setNomeAluno(txtNome.getText());
            aluno.setEmailAluno(txtEmail.getText());
            aluno.setCategoriaAluno("Aluno");
            aluno.setRaAluno(txtRa.getText());
            aluno.setCursoAluno(txtCurso.getText());
            aluno.setTurmaAluno(txtTurma.getText());
            
            
            try {
                alunoDAO.alterar(aluno);
            } catch (SQLException ex) {
                Logger.getLogger(AlunoView.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            atualizarTabelaAluno();
            preparaSalvareCancelar();
            desativaCampos();
            
            
        } 
       
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tbAlunoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAlunoMouseClicked

        txtCodigo.setText(tbAluno.getValueAt(tbAluno.getSelectedRow(),0).toString());
        try {   
            aluno = alunoDAO.AlunoById(Integer.parseInt(txtCodigo.getText()));
        } catch (SQLException ex) {
            Logger.getLogger(AlunoView.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNome.setText(aluno.getNomeAluno());
        txtRa.setText(aluno.getRaAluno());
        txtCurso.setText(aluno.getCursoAluno());
        txtTurma.setText(aluno.getTurmaAluno());
        txtEmail.setText(aluno.getEmailAluno());
        
        preparaSelecaoTabela();
    }//GEN-LAST:event_tbAlunoMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um aluno!");
        } else{
            aluno = new Aluno();
            aluno.setCodigoAluno(Integer.parseInt(txtCodigo.getText()));
            int confirma = JOptionPane.showConfirmDialog(null, "Deseja excluir: " +txtNome.getText());
            
            if(confirma ==0){
                
                try {
                    alunoDAO.excluir(aluno);
                } catch (SQLException ex) {
                    Logger.getLogger(AlunoView.class.getName()).log(Level.SEVERE, null, ex);
                }
               
                JOptionPane.showMessageDialog(null, "Excluido com sucesso!");
                
                atualizarTabelaAluno();
                limpaCamposAluno();
                preparaExcluir();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

